public class Armor {
    private int damageReduction;
    private int weight;


    public void setDamageReduction(int damageReduction) {
        this.damageReduction = damageReduction;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getDamageReduction() {
        return damageReduction;
    }
}
